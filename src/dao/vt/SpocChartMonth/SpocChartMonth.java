package dao.vt.SpocChartMonth;

public class SpocChartMonth {
	private int month;
	private int IT;
	private int DTT;
	private int VT;
	
	
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getIT() {
		return IT;
	}
	public void setIT(int iT) {
		IT = iT;
	}
	public int getDTT() {
		return DTT;
	}
	public void setDTT(int dTT) {
		DTT = dTT;
	}
	public int getVT() {
		return VT;
	}
	public void setVT(int vT) {
		VT = vT;
	}
	
	
	
}
